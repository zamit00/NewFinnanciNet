// פונקציה לפתיחת modal מידע מקצועי
function openProfessionalInfoModal(productType) {
    const modal = document.getElementById('professionalInfoModal');
    const content = professionalInfoContent[productType];
    
    if (!content) {
        console.warn('⚠️ לא נמצא תוכן עבור:', productType);
        return;
    }
    
    // בניית ה-HTML
    let html = `
        <button class="close-btn" onclick="closeProfessionalInfoModal()">
            <i class="fas fa-times"></i>
        </button>
        <h1><i class="${content.icon}"></i> ${content.title}</h1>
    `;
    
    // הוספת כל הסעקשנים
    content.sections.forEach(section => {
        html += '<section class="section';
        if (section.type === 'head') html += ' head';
        html += '">';
        
        if (section.title) {
            html += `<h2>`;
            if (section.icon) html += `<i class="${section.icon}"></i>`;
            html += `${section.title}</h2>`;
        }
        
        if (section.content) {
            html += `<p>${section.content}</p>`;
        }
        
        if (section.list) {
            html += '<ul>';
            section.list.forEach(item => {
                html += `<li>${item}</li>`;
            });
            html += '</ul>';
        }
        
        if (section.content2) {
            html += `<p>${section.content2}</p>`;
        }
        
        html += '</section>';
    });
    
    // הוספת footer
    if (content.footer) {
        html += `
            <div class="footer">
                <p>${content.footer}</p>
            </div>
        `;
    }
    
    // הצגת התוכן
    const cardElement = modal.querySelector('.professional-info-card');
    cardElement.innerHTML = html;
    
    // הצגת ה-modal
    modal.classList.add('show');
    document.body.style.overflow = 'hidden';
}

// פונקציה לסגירת modal מידע מקצועי
function closeProfessionalInfoModal() {
    const modal = document.getElementById('professionalInfoModal');
    modal.classList.remove('show');
    document.body.style.overflow = 'auto';
}

// סגירת ה-modal בלחיצה על הרקע
document.addEventListener('click', function(e) {
    const modal = document.getElementById('professionalInfoModal');
    if (e.target === modal) {
        closeProfessionalInfoModal();
    }
});

// סגירת ה-modal עם מקש ESC
document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape') {
        closeProfessionalInfoModal();
    }
});

